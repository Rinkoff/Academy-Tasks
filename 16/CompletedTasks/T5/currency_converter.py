def usd_to_bgn(amount):
    return amount * 1.70

def bgn_to_usd(amount):
    return amount / 1.70

def eur_to_bgn(amount):
    return amount * 1.95

def bgn_to_eur(amount):
    return amount / 1.95

def gbp_to_bgn(amount):
    return amount * 2.20

def bgn_to_gbp(amount):
    return amount / 2.20
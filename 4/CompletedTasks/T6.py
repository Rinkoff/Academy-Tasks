number = int(input("Enter a number:"))

if number == 0:
    print("Нула")
elif number == 1:
    print("Едно")
elif number == 2:
    print("Две")
elif number == 3:
    print("Три")
elif number == 4:
    print("Четири")
elif number == 5:
    print("Пет")
elif number == 6:
    print("Шест")
elif number == 7:
    print("Седем")
elif number == 8:
    print("Осем")
elif number == 9:
    print("Девет")
else:
    print("Невалидно число")